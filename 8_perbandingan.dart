//operator perbandingan hasilnya adalah bool

/* 

== sama dengan
!= tidak sama dengan
> lebih dari
< kurang dari
>= lebih sama dari
<= kurang sama dari

*/

void main() {
  print(10 == 10);
  print(10 != 10);
  print(10 < 10);
  print(10 > 10);
  print(10 >= 10);
  print(10 <= 10);

  print('bivo' == 'bivo');
  print('bivo' != 'bivo');
}
